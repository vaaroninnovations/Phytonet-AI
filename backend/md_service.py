def estimate_runtime(cfg: MDConfig, atoms: Optional[int] = None) -> Dict[str, float]:
    """Rough wallclock estimate (hours) for the requested production time.

    Empirical rule-of-thumb from GROMACS benchmarks (as of 2024):
      CPU-only (32-core Xeon):  ~5-15 ns/day for a 30k-atom solvated complex.
      GPU (RTX 3090 / A100):    ~100-200 ns/day.
    """
    atoms = atoms or 30000
    scale = atoms / 30000.0
    ns_per_day_cpu = 10.0 / scale
    ns_per_day_gpu = 150.0 / scale
    return {
        "cpu32": (cfg.production_ns / ns_per_day_cpu) * 24.0,
        "gpu":   (cfg.production_ns / ns_per_day_gpu) * 24.0,
        "atoms_assumed": atoms,
    }
