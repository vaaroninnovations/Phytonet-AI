def _ps_to_steps(ps: float, dt_fs: float) -> int:
    return int(ps * 1000.0 / dt_fs)


def build_md_project(compound: Dict[str, Any],
                     target: Dict[str, Any],
                     receptor_pdb_content: Optional[str],
                     ligand_smiles_or_mol2: Optional[str],
                     cfg: MDConfig) -> Tuple[str, bytes]:
    """Create a zip package containing every file needed for MD.

    Args:
      compound: {name, smiles}
      target:   {gene_symbol, uniprot_id, pdb_id}
      receptor_pdb_content: optional PDB text (from docking receptor)
      ligand_smiles_or_mol2: SMILES string — ACPYPE will produce mol2 on the user's side.
      cfg: MDConfig instance controlling times/temperature/etc.
    Returns:
      (project_name, zip_bytes)
    """
    project = f"md_{compound['name']}_x_{target.get('gene_symbol','')}_{uuid.uuid4().hex[:6]}"
    project = re.sub(r"[^A-Za-z0-9_.-]", "_", project)[:80]
    buf = io.BytesIO()

    dt_ns = cfg.dt_fs / 1000.0
    ctx = {
        "force_field": cfg.force_field,
        "water_model": cfg.water_model,
        "box_type": cfg.box_type,
        "padding": cfg.box_padding_nm,
        "p_ion": cfg.positive_ion,
        "n_ion": cfg.negative_ion,
        "ion_conc": cfg.ion_concentration,
        "em_steps": cfg.em_steps,
        "nvt_ps": cfg.nvt_ps,
        "npt_ps": cfg.npt_ps,
        "production_ns": cfg.production_ns,
        "temp": cfg.temperature_K,
        "pressure": cfg.pressure_bar,
        "dt_ns": dt_ns,
        "nvt_steps": _ps_to_steps(cfg.nvt_ps, cfg.dt_fs),
        "npt_steps": _ps_to_steps(cfg.npt_ps, cfg.dt_fs),
        "prod_steps": int(cfg.production_ns * 1_000_000.0 / cfg.dt_fs),
    }

    report = _md_report(compound, target, cfg, ctx)

    with zipfile.ZipFile(buf, "w", zipfile.ZIP_DEFLATED) as zf:
        base = f"{project}/"
        zf.writestr(base + "README.md", _md_readme(compound, target, cfg))
        zf.writestr(base + "MD_PREPARATION_REPORT.md", report)
        zf.writestr(base + "minim.mdp", MDP_MIN.format(**ctx))
        zf.writestr(base + "ions.mdp", MDP_MIN.format(**ctx))
        zf.writestr(base + "nvt.mdp", MDP_NVT.format(**ctx))
        zf.writestr(base + "npt.mdp", MDP_NPT.format(**ctx))
        zf.writestr(base + "md.mdp", MDP_PROD.format(**ctx))
        zf.writestr(base + "run_md.sh", BASH_SCRIPT.format(**ctx))
        zf.writestr(base + "run_md.ps1", POWERSHELL_SCRIPT.format(**ctx))
        zf.writestr(base + "merge_topology.py", MERGE_TOPOLOGY_PY)
        zf.writestr(base + "make_index_helper.txt", INDEX_HELPER)
        zf.writestr(base + "commands.txt", _commands_text(ctx))
        if receptor_pdb_content:
            zf.writestr(base + "receptor.pdb", receptor_pdb_content)
        if ligand_smiles_or_mol2:
            zf.writestr(base + "ligand.smi", ligand_smiles_or_mol2)
        zf.writestr(base + "PROJECT_MANIFEST.json", json.dumps({
            "project": project, "compound": compound, "target": target,
            "config": {k: getattr(cfg, k) for k in cfg.__dataclass_fields__},
            "runtime_estimate": estimate_runtime(cfg, 30000),
        }, indent=2))
    return project, buf.getvalue()


